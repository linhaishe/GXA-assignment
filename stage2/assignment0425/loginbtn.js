const mainBodyBox = document.getElementById("main-body");
// const signOutBtn = document.getElementById("login");

const loginFunc = function () {
  mainBodyBox.style.display == "block"
    ? (mainBodyBox.style.display = "none")
    : (mainBodyBox.style.display = "block");
};

const signOut = function () {
  mainBodyBox.style.display == "block"
    ? (mainBodyBox.style.display = "none")
    : (mainBodyBox.style.display = "block");
};
